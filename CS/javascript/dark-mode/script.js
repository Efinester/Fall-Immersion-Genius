const variable_name = document.body;
function darkMode(){
  if(variable_name.style.backgroundColor === 'white'){ 
        variable_name.style.backgroundColor === 'black';
        variable_name.style.Color === 'white';
  }
  else{
    variable_name.style.backgroundColor = 'white';
    variable_name.style.color = 'white';
  }
}
